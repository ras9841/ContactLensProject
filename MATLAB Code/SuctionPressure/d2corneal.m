function h = d2corneal(r)
    %h = -R.^2./((1-R.^2).^(3/2))-1./sqrt(1-R.^2);       
    %h = 0;
%    h0 = .5;
%    R = 1;
%    h = -(((h0^2+R^2)/(2*h0))^2-r.^2).^(-1/2)-(r.^2).*(((h0^2+R^2)/(2*h0))^2-r.^2).^(-3/2);
% %h = -1;
% 
%     omega = 1;
%     h = ((2*pi*omega)^2)*cos(2*pi*omega*r);

% %_________________________ USED IN PAPER __________________________
% 
%   ac = 1.37;
%   bc = 1.24;
%   
%   hs = 3.04;
%   as = 1.07;
%   
%       if r < -.894
%           h = -(((hs^2+as^2)/(2*hs))^2-r.^2).^(-1/2)-(r.^2).*(((hs^2+as^2)/(2*hs))^2-r.^2).^(-3/2);
%          
%       elseif r > .894
%           h =  -(((hs^2+as^2)/(2*hs))^2-r.^2).^(-1/2)-(r.^2).*(((hs^2+as^2)/(2*hs))^2-r.^2).^(-3/2);
%       else
%           h = -(ac/bc)^2*((ac)^2-(ac*r/bc).^2).^(-1/2) - (ac/bc)^4*(r.^2).*((ac)^2-(ac*r/bc).^2).^(-3/2);
%       end
      
      %%%%%% ------------- SMOOTHED OUT CORNEA -----------------------
  % Nondimensional:  -0.6cm < r < 0.6cm ---- cornea
  %                 cornea(r) = 0.96 ( 1cm^2 - (r/.87)^2 )^(1/2) cm
  %        -0.75 cm < r < -0.6cm AND  0.6cm <r < 0.75cm  ---- polynomial
%   c =
% 
%    1.0e+03 *
% 
%   -1.679743209891167
%    5.575474074123584
%   -7.367189925992693
%    4.844493100044825
%   -1.586613500014980
%    0.207994200001994
  %       r < -0.75cm AND  0.75cm <r   ---- sclera
    %

      if r < (-.75/.7)
           h = -(0.7)./(sqrt(1.2^2 - (.7*r).^2))-((0.7)^3*(r.^2))./((sqrt(1.2^2 - (.7*r).^2)).^3);
      elseif r > (.75/.7)
           h = -(0.7)./(sqrt(1.2^2 - (.7*r).^2))-((0.7)^3*(r.^2))./((sqrt(1.2^2 - (.7*r).^2)).^3);
      elseif r<(-.6/.7)
           h = (5*4*(-1.679743209891167e3)*(0.7*r).^3 + 4*3*(5.575474074123584e3)*(0.7*r).^2 ...
              + 3*2*(-7.367189925992693e3)*(0.7*r).^1 + 2*( 4.844493100044825e3))*0.7;
      elseif r>(.6/.7)
          h = (5*4*(-1.679743209891167e3)*(0.7*r).^3 + 4*3*(5.575474074123584e3)*(0.7*r).^2 ...
              + 3*2*(-7.367189925992693e3)*(0.7*r).^1 + 2*( 4.844493100044825e3))*0.7;
      else
          h = -((0.96)/(.87^2))*(0.7)./sqrt(1-(0.7*r/.87).^2) ...
              + ((0.96)/(.87^4))*(0.7^3)*(r.^2)./(sqrt(1-(0.7*r/.87).^2).^3);
      end     

end